def add_body(document: Document) -> None:
    content = full_report_content()
    for index, (title, paragraphs) in enumerate(content):
        is_major_heading = (
            title in {"ABSTRACT", "REFERENCES"}
            or bool(re.match(r"^\d+\. [A-Z]", title))
            or title.startswith("APPENDIX")
        )
        if index > 0 and is_major_heading:
            document.add_page_break()
        level = 1 if is_major_heading else 2
        add_heading(document, title, level=level, bookmark=bookmark_name(title))
        for paragraph in paragraphs:
            if title == "REFERENCES":
                p = document.add_paragraph(style=None)
                p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
                p.paragraph_format.line_spacing = 1.5
                r = p.add_run(paragraph)
                r.font.name = "Times New Roman"
                r.font.size = Pt(12)
            else:
                add_justified_paragraph(document, paragraph)
        if title == "6.1 TECHNOLOGY STACK AND PROJECT STRUCTURE":
            add_technology_table(document)
        if title == "APPENDIX E: IMPORTANT CODE SNIPPETS":
            for snippet_heading, snippet_code in CODE_SNIPPETS:
                add_code_block(document, snippet_heading, snippet_code)
        if title == "5. SYSTEM DESIGN":
            add_architecture_figure(document)
            add_workflow_figure(document)
            add_database_figure(document)
        if title == "7. RESULT":
            add_score_chart(document)


