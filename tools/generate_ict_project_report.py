def add_heading(document: Document, text: str, level: int = 1, bookmark: str | None = None) -> None:
    global BOOKMARK_ID
    paragraph = document.add_heading("", level=level)
    if bookmark:
        bookmark_start = OxmlElement("w:bookmarkStart")
        bookmark_start.set(qn("w:id"), str(BOOKMARK_ID))
        bookmark_start.set(qn("w:name"), bookmark)
        paragraph._p.append(bookmark_start)
    paragraph.add_run(text.upper())
    if bookmark:
        bookmark_end = OxmlElement("w:bookmarkEnd")
        bookmark_end.set(qn("w:id"), str(BOOKMARK_ID))
        paragraph._p.append(bookmark_end)
        BOOKMARK_ID += 1
    paragraph.alignment = WD_ALIGN_PARAGRAPH.LEFT
    for run in paragraph.runs:
        run.font.name = "Times New Roman"
        run.font.bold = True
        run.font.size = Pt(24 if level == 1 else 13)


