def add_front_matter(document: Document) -> None:
    add_heading(document, "LIST OF FIGURES")
    table = document.add_table(rows=1, cols=2)
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.style = "Table Grid"
    set_cell_text(table.rows[0].cells[0], "Figure No.", True)
    set_cell_text(table.rows[0].cells[1], "Title", True)
    for number, title in FIGURES:
        cells = table.add_row().cells
        set_cell_text(cells[0], number)
        set_cell_text(cells[1], title)
    document.add_page_break()

    add_heading(document, "LIST OF ABBREVIATIONS")
    table = document.add_table(rows=1, cols=2)
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.style = "Table Grid"
    set_cell_text(table.rows[0].cells[0], "Abbreviation", True)
    set_cell_text(table.rows[0].cells[1], "Expansion", True)
    for abbr, meaning in ABBREVIATIONS:
        cells = table.add_row().cells
        set_cell_text(cells[0], abbr)
        set_cell_text(cells[1], meaning)
    document.add_page_break()

    add_heading(document, "TABLE OF CONTENTS")
    for title, _ in full_report_content():
        is_subheading = bool(re.match(r"^\d+\.\d+", title))
        paragraph = document.add_paragraph()
        paragraph.paragraph_format.line_spacing = 1.5
        paragraph.paragraph_format.left_indent = Inches(0.28 if is_subheading else 0)
        paragraph.paragraph_format.tab_stops.add_tab_stop(
            Inches(6.25),
            alignment=WD_TAB_ALIGNMENT.RIGHT,
            leader=WD_TAB_LEADER.DOTS,
        )
        run = paragraph.add_run(title.title())
        run.bold = not is_subheading
        run.font.name = "Times New Roman"
        run.font.size = Pt(17 if not is_subheading else 10.5)
        paragraph.add_run("\t")
        page_run = paragraph.add_run(str(TOC_PAGE_NUMBERS.get(title.upper(), "")))
        page_run.bold = not is_subheading
        page_run.font.name = "Times New Roman"
        page_run.font.size = Pt(17 if not is_subheading else 10.5)
    document.add_page_break()


